#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_outcome_roundtrip() {
        let outcomes = vec![
            RetrospectiveOutcome::Success,
            RetrospectiveOutcome::Failure {
                reason: "build failed".to_string(),
            },
            RetrospectiveOutcome::Partial {
                completed: 3,
                total: 5,
            },
        ];
        for outcome in &outcomes {
            let json = serde_json::to_string(outcome).unwrap();
            let deserialized: RetrospectiveOutcome = serde_json::from_str(&json).unwrap();
            assert_eq!(*outcome, deserialized);
        }
    }

    #[test]
    fn test_outcome_as_str() {
        assert_eq!(RetrospectiveOutcome::Success.as_str(), "success");
        assert_eq!(
            RetrospectiveOutcome::Failure { reason: "x".into() }.as_str(),
            "failure"
        );
        assert_eq!(
            RetrospectiveOutcome::Partial {
                completed: 1,
                total: 2
            }
            .as_str(),
            "partial"
        );
    }

    #[test]
    fn test_metric_comparison_compute() {
        let cohort = vec![10.0, 20.0, 30.0, 40.0, 50.0];
        let mc = MetricComparison::compute(30.0, &cohort);
        assert!((mc.cohort_mean - 30.0).abs() < 0.01);
        assert!(mc.z_score.abs() < 0.01); // exactly at the mean
        assert!((mc.percentile - 0.6).abs() < 0.01); // 3 of 5 are <= 30
    }

    #[test]
    fn test_metric_comparison_empty_cohort() {
        let mc = MetricComparison::compute(42.0, &[]);
        assert_eq!(mc.cohort_mean, 0.0);
        assert_eq!(mc.percentile, 0.5);
    }
}
